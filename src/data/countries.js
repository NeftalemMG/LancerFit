// Fast lookup by stored code -> { label, flag }.
export const COUNTRY_BY_CODE = COUNTRIES.reduce((acc, c) => {
  acc[c.code] = c;
  return acc;
}, {});

// Helper: given a stored nationality code, return its flag emoji (or a globe).
export function flagFor(code) {
  if (!code) return "🌐";
  return COUNTRY_BY_CODE[String(code).toLowerCase()]?.flag || "🌐";
}

// Helper: display name for a code.
export function countryName(code) {
  if (!code) return "";
  return COUNTRY_BY_CODE[String(code).toLowerCase()]?.label || "";
}